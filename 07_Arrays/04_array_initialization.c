#include<stdio.h>

int main(){
    int a[] = {34,55,64};       // integer array
    printf("The value of a[0] is %d \n",a[0]);
    printf("The value of a[1] is %d \n",a[1]);
    printf("The value of a[2] is %d \n\n",a[2]);

    float b[] = {3.4,5.55,0.64};        // float array
    printf("The value of a[0] is %f \n",b[0]);
    printf("The value of a[1] is %f \n",b[1]);
    printf("The value of a[2] is %f \n",b[2]);
    return 0;
}